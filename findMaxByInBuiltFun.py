# 5. Write a Python program to find the maximum number in a list of numbers using a built-in
# function.
# Sample Input -> [10, 4, 45, 99, 12]
# Sample Output -> The maximum value is: 99

# Sample list
numbers = [10, 4, 45, 99, 12]

# Find the maximum value using the built-in max() function
max_value = max(numbers)

# Display the result
print("The maximum value is:", max_value)
